<p>
"The <a href="https://github.com/OxCGRT/covid-policy-tracker" target="_blank"
    >Oxford Covid-19 Government Response Tracker (OxCGRT)</a>
collects systematic information on which governments have taken which measures, and when.
This can help decision-makers and citizens understand governmental responses in a consistent way,
aiding efforts to fight the pandemic. The OxCGRT systematically collects information on several
different common policy responses governments have taken, records these policies on a scale to
reflect the extent of government action, and aggregates these scores into a suite of policy indices.
</p><p>
This is a project from the Blavatnik School of Government. More information on the OxCGRT is
available on the school's website:
<a href="https://www.bsg.ox.ac.uk/covidtracker" target="_blank">https://www.bsg.ox.ac.uk/covidtracker</a>.
This <a href="https://github.com/OxCGRT/covid-policy-tracker" target="_blank">README</a>
contains information about using the database."
</p>
<p>
"To help make sense of the data, we have produced four indices that aggregate the
data into a single number. Each of these indices report a number between 0 to 100 that
reflects the level of the governments response along certain dimensions. This is a measure
of how many of the relevant indicators a government has acted upon, and to what degree.
The index cannot say whether a government's policy has been implemented effectively."
</p><p>
